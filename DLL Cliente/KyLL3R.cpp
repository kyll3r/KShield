void KShield::Startup() {
	m_lProcessesList.clear();
	
	GetSystemProcessesList();
	GetExplorerProcessId();

	if(m_lProcessesList.empty())
	{
		MessageBox(0, "Não é possível executar o GunBound", "KyLL3R Game Shield!", MB_OK | MB_ICONSTOP);
		ExitProcess(1);
	}
}

void KShield::SystemProcessesScan() {
	for(std::list<PROCESSENTRY32>::iterator i = m_lProcessesList.begin(); i != m_lProcessesList.end(); i++)
	{
		if(ScanProcessMemory(i->th32ProcessID))
		{
			MessageBox(0, "Hack Detectado!\nPor favor feche todos os programas ilegais e tente executar o jogo novamente.", "KyLL3R Game Shield", MB_OK | MB_ICONSTOP);
			ExitProcess(4);
		}
	}
}

void KShield::CheckProcessOwner() {
	for(std::list<PROCESSENTRY32>::iterator i = m_lProcessesList.begin(); i != m_lProcessesList.end(); i++)
	{
		if(i->th32ProcessID == GetCurrentProcessId())
		{
			if(i->th32ParentProcessID != m_dwExplorerProcessId)
			{
				MessageBox(0, "Erro ao Inicializar a Proteção.", "KyLL3R Game Shield!", MB_OK | MB_ICONSTOP);
				ExitProcess(1);
			}
		}
	}
}

void KShield::Cleanup() {
	m_lProcessesList.clear();
}


